#Adding values to an empty vector using index

# create an empty numeric
a=c()

# display it
print(a)

# create an empty numeric
# vector b
b=c()

# display it
print(b)

# create an empty numeric
# vector d
d=c()

# display it
print(d)

# include numeric data into 
# vector a insert value 10 
# at location 1
a[1]=10

# insert value 20 at location 2
a[2]=20

# insert value 14.5 at location 3
a[3]=14.5

# insert value 89.000 at location 4
a[4]=89.000

# display vector a
print(a)

# include logical data into vector
# b at locations 1,2,3
b[1]=TRUE
b[2]=FALSE
b[3]=FALSE

# display vector b
print(b)

# include character data into vector 
# d at locations 1,2,3
d[1]="Sravan"
d[2]="Bobby"
d[3]="pinkey"

# display vector
print(d)